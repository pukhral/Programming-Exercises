#include <bits/stdc++.h>
using namespace std;

int main()
{
    cout << "Integer takes " << sizeof(int) << " bytes." << endl;
    cout << "Float takes " << sizeof(float) << " bytes." << endl;
    cout << "Double takes " << sizeof(double) << " bytes" << endl;
    cout << "Character takes " << sizeof(char) << " bytes" << endl;
    cout << "Boolean takes " << sizeof(bool) << " bytes" << endl;

    return 0;
}
